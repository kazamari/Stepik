'''
You are given two coordinates on a chess board of the first queen and two of the second. Find out, whether two queens
hit each other or not.

INPUT
Four integer numbers x1,y1,x2,y2 are being typed.

OUTPUT
Type "YES" (uppercase) if they hit each other or "NO" if the don't.

Sample Input 1:
    1 1 3 3

Sample Output 1:
    YES

Sample Input 2:
    1 1 4 3

Sample Output 2:
    NO

Sample Input 3:
    2 2 5 2

Sample Output 3:
    YES
'''
x1, y1, x2, y2 = map(int, input().split())
print('YES' if x1 == x2 or y1 == y2 or x1 - y1 == x2 - y2 or x1 + y1 == x2 + y2 else 'NO')
